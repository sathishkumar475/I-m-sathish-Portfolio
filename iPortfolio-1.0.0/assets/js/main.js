const downloadButton = document.querySelector(".download-button");

// Original Google Drive link
const originalLink =
  "https://drive.google.com/file/d/1a70t1CdKdL2U_L7Om1v6tpMp_yIj-9vu/view?usp=sharing";

// Convert Google Drive URL to direct download URL
const getDirectDownloadLink = (url) => {
  const match = url.match(/\/d\/(.*?)\//);
  return match && match[1]
    ? `https://drive.google.com/uc?export=download&id=${match[1]}`
    : url;
};

const fileLink = getDirectDownloadLink(originalLink);

// Download function with countdown
const initializeTimer = (e) => {
  e.preventDefault(); // stop instant navigation
  if (downloadButton.classList.contains("disable-timer")) {
    window.location.href = fileLink;
    return;
  }

  let timer = parseInt(downloadButton.dataset.timer, 10);
  downloadButton.classList.add("timer");
  downloadButton.innerHTML = `Your download will begin in <b>  ${timer}   </b> seconds...`;

  const initCounter = setInterval(() => {
    timer--;
    if (timer > 0) {
      downloadButton.innerHTML = `Your download will begin in <b>  ${timer}   </b> seconds...`;
    } else {
      clearInterval(initCounter);
      window.location.href = fileLink;
      downloadButton.innerText = "Resume is downloading...";
      setTimeout(() => {
        downloadButton.classList.replace("timer", "disable-timer");
        downloadButton.innerHTML = `
          <span class="icon material-symbols-rounded"></span>
          <span class="text">Download Again</span>`;
      }, 3000);
    }
  }, 1000);
};

downloadButton.addEventListener("click", initializeTimer);
